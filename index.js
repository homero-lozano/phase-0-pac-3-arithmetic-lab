function add(a, b) { 
    return a + b 
} 
function subtract(a, b) {
    return a - b 
} 
function multiply(a, b) {
    return a * b
} 
function divide(a, b) {
    return a / b 
} 
function increment(n) {
    return n + 1 
}
function decrement(n) {
    return n - 1
}
function makeInt(n) {
let Int = parseInt(n, 10) 
return Int 
}
function preserveDecimal(n) {
    let Decimal = parseFloat(n, 2.222)
    return Decimal
}